# 0-9
print('range(10)')
for i in range(10):
	print(i)
	
# 3-5
print('range(3, 6)')      
for i in range(3, 6):
     print(i)
     
# 4-8 Stepping by 2
print('range(4, 10 ,2)')     
for i in range(4, 10, 2):
     print(i)
     
# Stepping backward by 2
print('range(0, -10, -2)')       
for i in range(0, -10, -2):
    print(i)
